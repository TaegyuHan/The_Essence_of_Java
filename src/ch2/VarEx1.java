package ch2;

public class VarEx1 {

    public static void main(String[] args) {
        int yaer = 0;
        int age = 14;
        System.out.println(yaer);
        System.out.println(age);

        yaer = yaer + 2000;
        age = age + 1;
        System.out.println(yaer);
        System.out.println(age);
    }
}
